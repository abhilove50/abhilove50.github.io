//Sticky navbar js//
$(window).on('scroll',function() {
  var scroll = $(window).scrollTop();
  if (scroll < 600) {
   $("nav").removeClass("sticky");
  }else{
   $("nav").addClass("sticky");
   $("nav").removeClass("navbar-trans");
  }
});

